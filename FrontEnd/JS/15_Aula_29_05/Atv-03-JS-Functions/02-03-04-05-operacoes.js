function soma(x,y) {
    return x+y;
}

function subtracao(x,y) {
    return x-y;
}

function multiplicacao(x,y) {
    return x*y;
}

function divisao(x,y) {
    return x/y;
}

console.log(`A soma é: ${soma(3, 3)}`);
console.log(`A soma é: ${subtracao(3, 3)}`);
console.log(`A soma é: ${multiplicacao(3, 3)}`);
console.log(`A soma é: ${divisao(3, 3)}`);

